tipo = input()

if tipo == "demônio do fogo":
    print("A fraqueza do demônio é: água")
elif tipo == "demônio do gelo":
    print("A fraqueza do demônio é: fogo")
elif tipo == "demônio da escuridão":
    print("A fraqueza do demônio é: luz")
elif tipo == "demônio da fumaça":
    print("A fraqueza do demônio é: vento")
else:
    print("Não foi possível identificar a fraqueza do demônio")